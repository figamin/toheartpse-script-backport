#define KEYLEN 233

int main(int argc, char **argv) {
  FILE *infile = fopen(argv[1], "rb");
  FILE *outfile = fopen(argv[2], "wb");

  char readbuf[0xf2];
  fread(readbuf, 0xf1, 1, infile);
  fwrite(readbuf, 0xf1, 1, outfile);
  fclose(infile);

  char *tblbuf = calloc(24 * (argc - 3), sizeof(uint8_t));

  uint8_t c, outc;
  for (int i = 3; i < argc; i++) {
    size_t j = 0;
    size_t offset = ftell(outfile);
    FILE *fp = fopen(argv[i], "rb");
    while (1) {
      c = getc(fp);
      if (feof(fp)) {
        break;
      }
      outc = key[j++ % KEYLEN] + c;
      putc(outc, outfile);
    }
    const char *filename = basename(argv[i]);
    char *pos = strchr(filename, '.');
    char *entry = tblbuf + ((i - 3) * 24);
    memset(entry, 0x00, 24);
    memset(entry, 0x20, 8);
    for (size_t n = 0; filename + n != pos; n++) {
      entry[n] = filename[n];
    }
    for (size_t n = 0; n < 4; n++) {
      pos++;
      entry[n + 8] = *pos;
    }
    entry[12] = (uint8_t)offset;
    entry[13] = (uint8_t)(offset >> 8);
    entry[14] = (uint8_t)(offset >> 16);
    entry[15] = (uint8_t)(offset >> 24);
    entry[16] = (uint8_t)j;
    entry[17] = (uint8_t)(j >> 8);
    entry[18] = (uint8_t)(j >> 16);
    entry[19] = (uint8_t)(j >> 24);

    size_t noffset = offset + j;
    entry[20] = (uint8_t)noffset;
    entry[21] = (uint8_t)(noffset >> 8);
    entry[22] = (uint8_t)(noffset >> 16);
    entry[23] = (uint8_t)(noffset >> 24);

    fclose(fp);
  }

  size_t j = 0;
  for (int i = 3; i < argc; i++) {
    char *entry = tblbuf + ((i - 3) * 24);
    for (size_t n = 0; n < 24; n++) {
      outc = key[j++ % KEYLEN] + entry[n];
      putc(outc, outfile);
    }
  }

  uint8_t coda[] = {0xf6, 0xf9, 0xe6};
  for (size_t i = 0; i < 3; i++) {
    outc = key[j++ % KEYLEN] + coda[i];
    putc(outc, outfile);
  }

  free(tblbuf);
  fclose(outfile);
  return 0;
}
